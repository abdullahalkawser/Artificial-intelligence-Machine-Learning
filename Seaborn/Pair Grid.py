# ржкрзНрж░рзЯрзЛржЬржирзАрзЯ рж▓рж╛ржЗржмрзНрж░рзЗрж░рж┐ ржЗржорзНржкрзЛрж░рзНржЯ ржХрж░рж╛
import seaborn as sns
import matplotlib.pyplot as plt

# Seaborn ржПрж░ penguins ржбрзЗржЯрж╛рж╕рзЗржЯ рж▓рзЛржб ржХрж░рж╛
penguins = sns.load_dataset("penguins")

# ЁЯз╣ Missing values ржмрж╛ржж ржжрзЗрзЯрж╛ (PairGrid ржП error ржПрзЬрж╛рждрзЗ)
penguins = penguins.dropna()

# ЁЯОи PairGrid рждрзИрж░рж┐ ржХрж░рж╛
g = sns.PairGrid(
    data=penguins,
    vars=["bill_length_mm", "bill_depth_mm", "flipper_length_mm", "body_mass_g"],
    hue="species"
)

# ржирж┐ржЪрзЗрж░ ржЕржВрж╢рзЗ scatterplot ржжрзЗржЦрж╛ржирзЛ
g.map_lower(sns.scatterplot)

# ржЙржкрж░рзЗрж░ ржЕржВрж╢рзЗ KDE (density) ржжрзЗржЦрж╛ржирзЛ
g.map_upper(sns.kdeplot, fill=True)

# ржбрж╛рзЯрж╛ржЧрзЛржирж╛рж▓рзЗ histogram ржжрзЗржЦрж╛ржирзЛ
g.map_diag(sns.histplot, kde=True)

# рж▓рзЗржЬрзЗржирзНржб ржпрзЛржЧ ржХрж░рж╛
g.add_legend()

# ржЯрж╛ржЗржЯрзЗрж▓ ржпрзЛржЧ ржХрж░рж╛
plt.suptitle("ЁЯРз Penguins Dataset - PairGrid Visualization", fontsize=14, y=1.02)

# ржЪрж╛рж░рзНржЯ ржжрзЗржЦрж╛ржирзЛ
plt.show()



